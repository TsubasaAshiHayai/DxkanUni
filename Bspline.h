#ifndef B_SPLINE_H  // 二重読み込み防止（おまじない）
#define B_SPLINE_H

#include <math.h>

// -----------------------------------------------------------
// 定数定義 (すべてのファイルで共通して使う設定値)
// -----------------------------------------------------------
#define N 10            // 制御点の数
#define K 3             // 次数
#define NUM_KNOTS (N + K + 1) // ノットベクトルの長さ

// -----------------------------------------------------------
// 構造体定義
// -----------------------------------------------------------
typedef struct {
    double controlY[N];      // Y座標パラメータ
    double knots[NUM_KNOTS]; // ノットベクトル
} KanEdge;

// -----------------------------------------------------------
// 関数のプロトタイプ宣言 (「こういう関数があるよ」という紹介)
// -----------------------------------------------------------

// エッジを初期化する
void InitKanEdge(KanEdge* edge);

// AIの計算実行 (xを入力してyを受け取る)
double Forward(KanEdge* edge, double x);

// 基底関数の計算 (内部計算用だが、必要なら公開しても良い)
double BasisFunction(double* knots, int i, int k, double t);

#endif // B_SPLINE_H の終わり